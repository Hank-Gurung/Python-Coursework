
def items_list(): 
    file = open("data.txt","r")
    items=[]
    for line in file.readlines():
        items.append(line.strip( ).split(","))
    file.close()
    return items     

    # for table
def table():
    items=items_list()
    print("-------------------------------------------------------------------------------------------------------------------------")#112
    print('S.N'.ljust(4),'||','Name'.ljust(15),'||','Brand '.ljust(15),'||','Price'.ljust(15),'||','Quantity'.ljust(15),'||','Processor'.ljust(15),'||','Graphics card'.ljust(15),'||')
    print("-------------------------------------------------------------------------------------------------------------------------")
    for i in range (len(items)):
        print(str(i+1).ljust(4),'||',end="")
        for j in range (len(items[i])):
            print(items[i][j].ljust(16),"||",end="")
        print()
        print("-------------------------------------------------------------------------------------------------------------------------")
    print("")
    
